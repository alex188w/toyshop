package example.toyshop.model;

/**
 * Статусы корзины покупок.
 */
public enum CartStatus {

    /**
     * Текущая активная корзина, в которой пользователь может добавлять товары.
     */
    ACTIVE,

    /**
     * Оформленный заказ — корзина, по которой завершена покупка.
     */
    COMPLETED
}
